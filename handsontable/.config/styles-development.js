const path = require('path');
const rspack = require('@rspack/core');
const configFactory = require('./base');
const removeEmptyScripts = require('./plugin/rspack/remove-empty-scripts');
const addCssToJsExport = require('./add-css-to-js-export');

module.exports.create = function create(envArgs) {
  const config = configFactory.create(envArgs);

  // Config for CSS extraction to handsontable/styles/
  config.forEach(function (c) {
    c.devtool = false;
    c.entry = {
      handsontable: './src/styles/handsontable.scss',
    };
    // Redirect stub JS files to a temp directory so they don't overwrite dist/ bundles
    c.output.path = path.resolve(__dirname, '../tmp_styles');
    c.output.filename = '[name].stub.js';

    c.module = {
      rules: [
        {
          test: /\.scss$/,
          use: [
            { loader: rspack.CssExtractRspackPlugin.loader },
            { loader: 'css-loader' },
            { loader: 'sass-loader', options: { sassOptions: { silenceDeprecations: ['legacy-js-api'] } } },
          ]
        }
      ],
    };

    c.plugins.push(
      removeEmptyScripts(),
      new rspack.CssExtractRspackPlugin({
        filename: '../styles/handsontable.css',
      }),
    );
  });

  addCssToJsExport(config, {
    cssFilename: '../styles/handsontable.css',
    banner: '/* This file is autogenerated. */',
  });

  // Keep the TypeScript twin in sync so `import handsontableStyles from '../styles/handsontableStyles'`
  // resolves to the freshly-built CSS (TypeScript prefers .ts over .js during compilation).
  addCssToJsExport(config, {
    cssFilename: '../styles/handsontable.css',
    outputJsPath: require('path').resolve(__dirname, '../src/styles/handsontableStyles.ts'),
    banner: '/* This file is autogenerated. */',
  });

  return config;
}

