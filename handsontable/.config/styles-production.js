const path = require('path');
const rspack = require('@rspack/core');
const configFactory = require('./styles-development');
const addCssToJsExport = require('./add-css-to-js-export');
const { BROWSERS_LIST } = require('../../browser-targets.js');

const AUTO_GENERATED_BANNER = '/* This file is autogenerated. */';
const CSS_FILENAME = '../styles/handsontable.min.css';

module.exports.create = function create(envArgs) {
  const config = configFactory.create(envArgs);

  config.forEach(function (c) {
    c.mode = 'production';

    c.optimization = {
      minimize: true,
      minimizer: [
        new rspack.LightningCssMinimizerRspackPlugin({
          minimizerOptions: {
            targets: BROWSERS_LIST.map(b => b.toLowerCase()),
          },
        }),
      ],
    };

    // Extract CSS to styles/ and plugin writes JS to src/styles as extra file
    c.plugins = c.plugins.filter(plugin => {
      if (plugin instanceof rspack.CssExtractRspackPlugin) return false;
      if (plugin.__isCssToJsExport) return false; // remove dev plugin added by styles-development
      return true;
    });
    c.plugins.push(
      new rspack.CssExtractRspackPlugin({
        filename: CSS_FILENAME,
      }),
    );
  });

  addCssToJsExport(config, {
    cssFilename: CSS_FILENAME,
    outputJsPath: path.resolve(__dirname, '../src/styles/handsontableStyles.js'),
    banner: AUTO_GENERATED_BANNER,
  });

  // Keep the TypeScript twin in sync (TypeScript prefers .ts over .js during compilation).
  addCssToJsExport(config, {
    cssFilename: CSS_FILENAME,
    outputJsPath: path.resolve(__dirname, '../src/styles/handsontableStyles.ts'),
    banner: AUTO_GENERATED_BANNER,
  });

  return config;
}

